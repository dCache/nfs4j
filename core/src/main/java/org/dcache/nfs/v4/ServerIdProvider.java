package org.dcache.nfs.v4;

import org.dcache.nfs.v4.xdr.server_owner4;

/**
 * Interface to provide server unique information used during EXCHANGE_ID operation.
 *
 * The server scope allows a group of servers to indicate to clients that a set of servers sharing the same server scope
 * value has arranged to use compatible values of otherwise opaque identifiers. Thus, the identifiers generated by one
 * server of that set may be presented to another of that same scope.
 *
 * See rfc5661 section 2.10.4 for details.
 */
public interface ServerIdProvider {

    server_owner4 getOwner();

    byte[] getScope();
}
